# What information is lost during which conversions?

a = 2
b = 1
c = b/a

print('Convert an int to a float')
print(a)
print(type(a))
print(float(a))
print(type(float(a)))
print('Convert a float to an int')
print(b)
print(type(b))
print(int(b))
print(type(int(b)))
print('Perform division using a float and an int.')
print(c)
print(type(c))
